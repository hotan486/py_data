###################################
# Set 집합 {} key 가 없음
###################################

set1 = {1,2,3}
set2 = {3,2,1}
set3 = {1,2,2,3,3,3}

###################################
# 인덱스가 없음 true
###################################
print(set1 == set2)
print(set2 == set3)

###################################
# 합집합, 교집합, 차집합
###################################
set4 = {1,2,3,4,5}
set5 = {4,5,6,7}

print(set4 | set5)
print(set4 & set5)
print(set4 - set5)
print(set5 - set4)
print(set4.difference(set5))